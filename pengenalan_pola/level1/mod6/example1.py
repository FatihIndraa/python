import math
# pow() function
print("pow()")
res1 = math.pow(2, 4)
res2 = math.pow(5, 3)
res3 = math.pow(10, 3)
print("res1 = ", res1)
print("res2 = ", res2)
print("res3 = ", res3)
print()
# sqrt() function
print("sqrt()")
res1 = math.sqrt(2)
res2 = math.sqrt(3)
res3 = math.sqrt(1000)
print("res1 = ", res1)
print("res2 = ", res2)
print("res3 = ", res3)
print()
# ceil() function
print("ceil()")
res1 = math.ceil(100.001)
res2 = math.ceil(-1.001)
res3 = math.ceil(-1.99)
print("res1 = ", res1)
print("res2 = ", res2)
print("res3 = ", res3)
print()
